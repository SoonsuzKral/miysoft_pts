function loadSection(section) {
    // Sekmeleri göster/gizle
    document.querySelectorAll('.cms-section').forEach(el => el.classList.add('hidden'));
    const target = document.getElementById(`section-${section}`);
    if (target) target.classList.remove('hidden');

    if (section === 'blog') { loadBlogs(); return; }
    if (section === 'partners') { loadPartners(); return; }

    const def = SECTIONS[section];
    if (!def) return;

    axios.get(CMS_URLS.contents, { params: { section } }).then(res => {
        const data = {};
        res.data.data.forEach(item => data[item.key] = item.value);
        sectionData[section] = data;

        const container = document.getElementById(`${section}-fields`);
        if (!container) return;

        container.innerHTML = def.map(field => `
            <div>
                <label class="block text-sm font-medium text-gray-700 mb-1">${field.label}</label>
                ${field.type === 'textarea' || field.type === 'html'
                    ? `<textarea name="${field.key}" rows="${field.type === 'html' ? 8 : 3}" class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]">${data[field.key] || ''}</textarea>`
                    : `<input type="${field.type === 'url' ? 'url' : 'text'}" name="${field.key}" value="${(data[field.key] || '').replace(/"/g, '&quot;')}" class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]">`
                }
            </div>
        `).join('');
    });
}

function saveSection(section) {
    const def = SECTIONS[section];
    if (!def) return;

    const items = def.map(field => {
        const el = document.querySelector(`[name="${field.key}"]`);
        return { key: field.key, value: el?.value || '', type: field.type };
    });

    axios.post(CMS_URLS.upsert, { items }).then(res => {
        toast('success', res.data.message);
    });
}

