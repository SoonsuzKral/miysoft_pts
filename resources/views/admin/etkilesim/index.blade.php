<script>
const ANN_URLS = {
    list:    '{{ route("admin.interactions.announcements.index") }}',
    store:   '{{ route("admin.interactions.announcements.store") }}',
    update:  id => `/admin/interactions/announcements/${id}`,
    destroy: id => `/admin/interactions/announcements/${id}`,
    polls:   '{{ route("admin.interactions.polls.index") }}',
    pollStore: '{{ route("admin.interactions.polls.store") }}',
};

const typeConfig = {
    general: { label: 'Genel',    bg: 'bg-gray-100',   text: 'text-gray-600',   dot: 'bg-gray-400' },
    urgent:  { label: 'Acil',     bg: 'bg-red-100',    text: 'text-red-700',    dot: 'bg-red-500' },
    event:   { label: 'Etkinlik', bg: 'bg-blue-100',   text: 'text-blue-700',   dot: 'bg-blue-500' },
};

function setEtkilesimTab(tab) {
    ['announcements','polls','pinned'].forEach(t => {
        document.getElementById(`view-${t}`).classList.toggle('hidden', t !== tab);
        const btn = document.getElementById(`etab-${t}`);
        if (btn) {
            btn.classList.toggle('border-[#02E0FB]', t === tab);
            btn.classList.toggle('text-[#02E0FB]', t === tab);
            btn.classList.toggle('border-transparent', t !== tab);
            btn.classList.toggle('text-gray-500', t !== tab);
        }
    });
    if (tab === 'polls') loadPolls();
    if (tab === 'pinned') loadAnnouncements(true);
    if (tab === 'announcements') loadAnnouncements();
}

document.addEventListener('DOMContentLoaded', loadAnnouncements);

function loadAnnouncements(pinnedOnly = false) {
    const params = {
        type: document.getElementById('annTypeFilter')?.value || '',
        pinned_only: pinnedOnly,
        per_page: 20,
    };
    axios.get(ANN_URLS.list, { params }).then(res => {
        const container = document.getElementById(pinnedOnly ? 'pinnedList' : 'announcementsList');
        if (!container) return;
        if (!res.data.data.length) {
            container.innerHTML = `<div class="text-center py-12 text-gray-400">Duyuru bulunamadı</div>`;
            return;
        }
        container.innerHTML = res.data.data.map(a => {
            const tc = typeConfig[a.type] || typeConfig.general;
            return `
            <div class="bg-white rounded-xl border border-gray-100 shadow-sm p-5 hover:shadow-md transition-shadow ${a.is_pinned ? 'border-l-4 border-l-[#FA6001]' : ''}">
                <div class="flex items-start justify-between gap-3">
                    <div class="flex-1">
                        <div class="flex items-center gap-2 mb-2">
                            ${a.is_pinned ? '<span class="text-sm">📌</span>' : ''}
                            <span class="inline-flex items-center gap-1.5 px-2 py-0.5 rounded-full text-xs font-medium ${tc.bg} ${tc.text}">
                                <span class="w-1.5 h-1.5 rounded-full ${tc.dot}"></span>${tc.label}
                            </span>
                            ${a.is_expired ? '<span class="text-xs text-gray-400 italic">Süresi doldu</span>' : ''}
                        </div>
                        <h3 class="font-semibold text-gray-800 mb-1">${a.title}</h3>
                        <p class="text-sm text-gray-500 line-clamp-2">${a.content.replace(/<[^>]*>/g,'')}</p>
                        <p class="text-xs text-gray-400 mt-2">${new Date(a.created_at).toLocaleDateString('tr-TR')}</p>
                    </div>
                    <div class="flex gap-1 shrink-0">
                        <button onclick="togglePin(${a.id}, ${a.is_pinned ? 0 : 1})" class="p-1.5 text-gray-400 hover:text-[#FA6001] hover:bg-orange-50 rounded-lg" title="${a.is_pinned ? 'Sabitlemeyi Kaldır' : 'Sabitle'}">
                            <svg class="w-4 h-4" fill="${a.is_pinned ? 'currentColor' : 'none'}" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M5 5a2 2 0 012-2h10a2 2 0 012 2v16l-7-3.5L5 21V5z"/></svg>
                        </button>
                        <button onclick="confirmDelete(ANN_URLS.destroy(${a.id}), loadAnnouncements)" class="p-1.5 text-gray-400 hover:text-red-500 hover:bg-red-50 rounded-lg">
                            <svg class="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16"/></svg>
                        </button>
                    </div>
                </div>
            </div>`;
        }).join('');
    });
}

function togglePin(id, pin) {
    axios.put(ANN_URLS.update(id), { is_pinned: pin }).then(() => { loadAnnouncements(); });
}

function openCreateAnnouncement() {
    Swal.fire({
        title: 'Yeni Duyuru',
        html: `
            <input id="annTitle" type="text" class="swal2-input" placeholder="Duyuru başlığı" style="display:flex">
            <textarea id="annContent" class="swal2-textarea" placeholder="Duyuru içeriği..." style="display:flex;height:120px"></textarea>
            <select id="annType" class="swal2-input" style="display:flex">
                <option value="general">Genel</option>
                <option value="urgent">Acil</option>
                <option value="event">Etkinlik</option>
            </select>
            <label style="display:flex;align-items:center;gap:8px;margin-top:8px;font-size:14px">
                <input type="checkbox" id="annPublished" checked> Hemen Yayınla
            </label>`,
        showCancelButton: true,
        confirmButtonColor: '#02E0FB',
        confirmButtonText: 'Oluştur',
        cancelButtonText: 'İptal',
        width: 600,
        preConfirm: () => {
            const title = document.getElementById('annTitle').value;
            const content = document.getElementById('annContent').value;
            if (!title || !content) { Swal.showValidationMessage('Başlık ve içerik zorunludur.'); return false; }
            return {
                title, content,
                type: document.getElementById('annType').value,
                is_published: document.getElementById('annPublished').checked ? 1 : 0,
            };
        }
    }).then(r => {
        if (r.isConfirmed) {
            axios.post(ANN_URLS.store, r.value).then(res => {
                toast('success', res.data.message);
                loadAnnouncements();
            });
        }
    });
}

function loadPolls() {
    axios.get(ANN_URLS.polls).then(res => {
        const grid = document.getElementById('pollsList');
        if (!res.data.data.length) {
            grid.innerHTML = `<div class="col-span-2 text-center py-12 text-gray-400">Anket bulunamadı</div>`;
            return;
        }
        grid.innerHTML = res.data.data.map(p => {
            const total = (p.results || []).reduce((a, b) => a + b, 0);
            return `
            <div class="bg-white rounded-xl border border-gray-100 shadow-sm p-5">
                <div class="flex items-start justify-between mb-4">
                    <h3 class="font-semibold text-gray-800 text-sm leading-snug">${p.question}</h3>
                    <span class="text-xs text-gray-400 shrink-0 ml-2">${p.responses_count || 0} oy</span>
                </div>
                <div class="space-y-2">
                    ${(p.options || []).map((opt, i) => {
                        const count = (p.results || [])[i] || 0;
                        const pct = total > 0 ? Math.round(count/total*100) : 0;
                        return `
                        <div>
                            <div class="flex justify-between text-xs mb-1">
                                <span class="text-gray-600">${opt}</span>
                                <span class="font-medium text-gray-800">${pct}%</span>
                            </div>
                            <div class="h-2 bg-gray-100 rounded-full overflow-hidden">
                                <div class="h-2 bg-[#02E0FB] rounded-full" style="width:${pct}%"></div>
                            </div>
                        </div>`;
                    }).join('')}
                </div>
                <div class="flex items-center justify-between mt-4 pt-3 border-t border-gray-100">
                    <span class="inline-flex px-2 py-0.5 rounded-full text-xs font-medium ${p.is_active ? 'bg-green-100 text-green-700' : 'bg-gray-100 text-gray-500'}">
                        ${p.is_active ? 'Aktif' : 'Bitti'}
                    </span>
                    <span class="text-xs text-gray-400">${p.ends_at ? 'Bitiş: ' + new Date(p.ends_at).toLocaleDateString('tr-TR') : 'Süresiz'}</span>
                </div>
            </div>`;
        }).join('');
    });
}

function openCreatePoll() {
    document.getElementById('modalTitle').textContent = 'Yeni Anket Oluştur';
    document.getElementById('modalBody').innerHTML = `
        <div class="space-y-4">
            <div>
                <label class="block text-sm font-medium text-gray-700 mb-1">Anket Sorusu <span class="text-red-500">*</span></label>
                <input type="text" id="pollQuestion" class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]" placeholder="Anket sorusunu yazın...">
            </div>
            <div>
                <label class="block text-sm font-medium text-gray-700 mb-2">Seçenekler <span class="text-red-500">*</span></label>
                <div id="pollOptions" class="space-y-2">
                    <input type="text" class="poll-option w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]" placeholder="Seçenek 1">
                    <input type="text" class="poll-option w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]" placeholder="Seçenek 2">
                </div>
                <button onclick="document.getElementById('pollOptions').insertAdjacentHTML('beforeend','<input type=text class=\\'poll-option w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB] mt-2\\' placeholder=\\'Seçenek ekle...\\'>')"
                    class="mt-2 text-xs text-[#02E0FB] hover:underline">+ Seçenek Ekle</button>
            </div>
            <div>
                <label class="block text-sm font-medium text-gray-700 mb-1">Bitiş Tarihi (isteğe bağlı)</label>
                <input type="datetime-local" id="pollEndsAt" class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]">
            </div>
        </div>`;
    document.getElementById('modalFooter').innerHTML = `
        <button onclick="document.getElementById('globalModal').classList.add('hidden')" class="px-4 py-2 text-sm text-gray-600 hover:bg-gray-100 rounded-lg">İptal</button>
        <button onclick="submitPoll()" class="px-4 py-2 text-sm text-white bg-[#FA6001] hover:bg-orange-500 rounded-lg font-medium">Anketi Yayımla</button>`;
    document.getElementById('globalModal').classList.remove('hidden');
}

function submitPoll() {
    const question = document.getElementById('pollQuestion').value;
    const options = [...document.querySelectorAll('.poll-option')].map(el => el.value).filter(v => v.trim());
    if (!question || options.length < 2) { toast('warning', 'Soru ve en az 2 seçenek zorunludur.'); return; }
    axios.post(ANN_URLS.pollStore, {
        question, options,
        ends_at: document.getElementById('pollEndsAt').value || null,
    }).then(res => {
        document.getElementById('globalModal').classList.add('hidden');
        toast('success', res.data.message);
        loadPolls();
    });
}
