<h3>🚗 Araç Yönetimi</h3>

<p>Araç Yönetimi modülü, şirket araç filosunun (fleet management) kayıt altına alınması, personellere tahsis edilmesi ve bakım süreçlerinin takip edilmesini sağlar. Her araç için plaka, marka, model, model yılı, yakıt türü, sigorta bilgileri, muayene tarihi ve kilometre bilgileri tutulur.</p>

<p>Araç-personel ataması yapılarak hangi aracın hangi personelde olduğu takip edilir. Bakım hatırlatıcıları sayesinde periyodik bakım, muayene ve sigorta yenileme tarihleri kaçırılmaz. Araç yakıt takibi, km başına yakıt tüketimi ve toplam işletme maliyeti raporlanabilir.</p>

