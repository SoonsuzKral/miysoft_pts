<script>
const DASH_URLS = {
    widgets:  '{{ route("admin.dashboard.widgets") }}',
    chart:    '{{ route("admin.dashboard.chart") }}',
    activity: '{{ route("admin.dashboard.activity") }}',
    refresh:  '{{ route("admin.dashboard.refresh") }}',
};

let trendChart = null;

// ─── İlk Yükleme ─────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
    loadWidgets();
    loadChartData();
    loadActivity();
    document.getElementById('lastRefresh').textContent = 'Son güncelleme: ' + new Date().toLocaleTimeString('tr-TR');
});

// ─── KPI Widgets ─────────────────────────────────────────────────────────────
function loadWidgets() {
    axios.get(DASH_URLS.widgets).then(res => {
        const d = res.data;

        setText('kpi-total',    d.total_personel);
        setText('kpi-checkins', d.today_checkins);
        setText('kpi-leave',    d.today_on_leave);
        setText('kpi-pending',  d.total_pending);
        setText('kpi-assets',   d.available_assets);
        setText('kpi-warranty', d.warranty_expiring_assets);

        // Onay şeridi
        setText('kpi-leave',    d.pending_leaves);
        setText('kpi-advances', d.pending_advances);
        setText('kpi-expenses', d.pending_expenses);

        // Bekleyen onay pulse göster/gizle
        const dot = document.getElementById('pendingDot');
        if (dot) dot.style.display = d.total_pending > 0 ? 'block' : 'none';

        // Departman bar chart
        renderDeptChart(d.department_stats);
    });
}

function setText(id, val) {
    const el = document.getElementById(id);
    if (el) el.textContent = val ?? '—';
}

// ─── Departman Bar Chart ─────────────────────────────────────────────────────
function renderDeptChart(stats) {
    const container = document.getElementById('deptChart');
    if (!container || !stats?.length) return;

    const max = Math.max(...stats.map(s => s.count), 1);
    const colors = ['#02E0FB','#FA6001','#10b981','#8b5cf6','#f59e0b','#ec4899','#06b6d4','#84cc16'];

    container.innerHTML = stats.map((s, i) => `
        <div>
            <div class="flex items-center justify-between text-xs mb-1">
                <span class="text-gray-600 font-medium truncate max-w-[120px]" title="${s.name}">${s.name}</span>
                <span class="font-bold text-gray-800 ml-2">${s.count}</span>
            </div>
            <div class="h-2.5 bg-gray-100 rounded-full overflow-hidden">
                <div class="h-2.5 rounded-full transition-all duration-700" style="width: ${Math.round(s.count/max*100)}%; background: ${colors[i % colors.length]}"></div>
            </div>
        </div>`).join('');
}

// ─── 6 Aylık Trend Chart ─────────────────────────────────────────────────────
function loadChartData() {
    axios.get(DASH_URLS.chart).then(res => {
        const { labels, ciData, lvData, nhData } = res.data;

        if (trendChart) trendChart.destroy();

        trendChart = new Chart(document.getElementById('trendChart'), {
            type: 'line',
            data: {
                labels,
                datasets: [
                    {
                        label: 'Giriş Kaydı',
                        data: ciData,
                        borderColor: '#02E0FB',
                        backgroundColor: 'rgba(2,224,251,.1)',
                        borderWidth: 2.5,
                        fill: true,
                        tension: 0.4,
                        pointBackgroundColor: '#02E0FB',
                        pointRadius: 4,
                    },
                    {
                        label: 'Onaylı İzin',
                        data: lvData,
                        borderColor: '#FA6001',
                        backgroundColor: 'rgba(250,96,1,.08)',
                        borderWidth: 2.5,
                        fill: false,
                        tension: 0.4,
                        pointBackgroundColor: '#FA6001',
                        pointRadius: 4,
                    },
                    {
                        label: 'Yeni İşe Alım',
                        data: nhData,
                        borderColor: '#10b981',
                        backgroundColor: 'rgba(16,185,129,.08)',
                        borderWidth: 2.5,
                        fill: false,
                        tension: 0.4,
                        pointBackgroundColor: '#10b981',
                        pointRadius: 4,
                    }
                ]
            },
            options: {
                responsive: true,
                maintainAspectRatio: false,
                interaction: { mode: 'index', intersect: false },
                plugins: {
                    legend: { display: false },
                    tooltip: {
                        backgroundColor: 'rgba(15,23,42,.9)',
                        titleColor: '#fff',
                        bodyColor: '#94a3b8',
                        padding: 12,
                        cornerRadius: 10,
                    }
                },
                scales: {
                    y: {
                        beginAtZero: true,
                        grid: { color: 'rgba(0,0,0,.05)' },
                        ticks: { color: '#94a3b8', font: { size: 11 } }
                    },
                    x: {
                        grid: { display: false },
                        ticks: { color: '#94a3b8', font: { size: 11 } }
                    }
                }
            }
        });
    });
}

// ─── Son Aktiviteler ─────────────────────────────────────────────────────────
function loadActivity() {
    axios.get(DASH_URLS.activity, { params: { limit: 8 } }).then(res => {
        const list = document.getElementById('activityList');
        if (!res.data.data.length) {
            list.innerHTML = '<p class="text-center text-gray-400 py-6 text-sm">Aktivite kaydı bulunamadı</p>';
            return;
        }

        const actionIcons = {
            'personel': '👤', 'leave': '📅', 'asset': '📦',
            'advance': '💰', 'expense': '🧾', 'shift': '🔄',
            'company': '🏢', 'department': '📂',
        };

        list.innerHTML = res.data.data.map(a => {
            const icon = Object.entries(actionIcons).find(([k]) => a.action.includes(k))?.[1] ?? '📋';
            const actionParts = a.action.split('.');
            const label = actionParts.map(p => p.charAt(0).toUpperCase() + p.slice(1)).join(' → ');
            return `
            <div class="flex items-start gap-3 py-2 border-b border-gray-50 last:border-0">
                <div class="w-8 h-8 rounded-full bg-gray-100 flex items-center justify-center text-base shrink-0">${icon}</div>
                <div class="flex-1 min-w-0">
                    <p class="text-sm font-medium text-gray-700 truncate">${label}</p>
                    <p class="text-xs text-gray-400">${a.time_full} · IP: ${a.ip ?? '—'}</p>
                </div>
                <span class="text-xs text-gray-300 shrink-0">${a.time}</span>
            </div>`;
        }).join('');
    }).catch(() => {});
}

// ─── Manuel Yenileme ─────────────────────────────────────────────────────────
function refreshDashboard() {
    const icon = document.getElementById('refreshIcon');
    icon.classList.add('animate-spin');
    axios.post(DASH_URLS.refresh).then(() => {
        loadWidgets();
        loadChartData();
        loadActivity();
        document.getElementById('lastRefresh').textContent = 'Son güncelleme: ' + new Date().toLocaleTimeString('tr-TR');
        setTimeout(() => icon.classList.remove('animate-spin'), 800);
    });
}
