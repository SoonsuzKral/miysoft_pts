<h3>📋 Abonelik Yönetimi</h3>

<p>Abonelik Yönetimi modülü, sistemin lisans ve abonelik bilgilerinin yönetildiği bölümdür. Mevcut abonelik planı, kalan personel kotası, kullanılan depolama alanı, lisans süresi ve yenileme tarihi gibi bilgiler bu modülde görüntülenir. Farklı abonelik planları (temel, profesyonel, kurumsal) arasında karşılaştırma yapılabilir.</p>

<p>Ödeme geçmişi, fatura bilgileri ve abonelik yenileme işlemleri takip edilebilir. Abonelik süresi dolmadan önce otomatik hatırlatma bildirimi gönderilir. Kullanım istatistikleri (aktif personel sayısı, depolama kullanımı, API çağrı sayısı) grafiksel olarak görüntülenebilir.</p>

