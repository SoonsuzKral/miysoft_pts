                    <div class="w-8 h-8 rounded-full bg-[#02E0FB]/15 text-[#02E0FB] font-bold text-xs flex items-center justify-center">
                        ${(r.personel?.first_name?.[0] || '') + (r.personel?.last_name?.[0] || '')}
                    </div>
                    <span class="font-medium text-gray-800 text-sm">${r.personel?.first_name || ''} ${r.personel?.last_name || ''}</span>
