<h3>🏠 Genel Bakış - Dashboard</h3>

<p>Sisteme giriş yaptıktan sonra karşılaşacağınız ilk ekran Dashboard sayfasıdır. Bu sayfa, sistem genelindeki önemli bilgilere hızlıca erişmenizi sağlayan widget'lar ve istatistik kartlarından oluşur. Üst kısımda toplam personel sayısı, bugünkü devamsızlık oranı, bekleyen izin talepleri ve yaklaşan doğum günleri gibi özet bilgiler görüntülenir.</p>

<p>Dashboard, kullanıcının rolüne göre özelleştirilmiş bilgiler sunar. Yöneticiler tüm şirket genelindeki verileri görürken, departman yöneticileri kendi departmanlarına ait bilgileri görüntüleyebilir. Sayfa ayrıca anlık bildirimler, hızlı işlem butonları ve takvim widget'ı içerir.</p>

