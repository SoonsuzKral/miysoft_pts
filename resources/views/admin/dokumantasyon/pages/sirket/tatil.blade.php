<h4>Özellikler</h4>
<ul>
  <li>Resmî tatil günlerini otomatik ekleme</li>
  <li>Şirket bazlı özel tatil tanımlama</li>
  <li>Tekrarlanan tatil günleri (yıllık periyot)</li>
  <li>Puantaj ve izin modülleri ile entegrasyon</li>
  <li>Takvim üzerinde renk kodlu gösterim</li>
</ul>

