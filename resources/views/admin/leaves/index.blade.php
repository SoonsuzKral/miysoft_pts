            </td>
            <td class="px-4 py-3">
                <span class="inline-flex items-center px-2 py-0.5 rounded-full text-xs font-medium ${sc.bg} ${sc.text}">${sc.label}</span>
            </td>
            <td class="px-4 py-3 text-gray-500 text-sm">${r.approver ? `${r.approver.name}` : '—'}</td>
