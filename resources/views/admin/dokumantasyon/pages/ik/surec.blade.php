<h4>Onboarding Adımları</h4>

<ol>
  <li><strong>Oryantasyon Eğitimi</strong> — Şirket tanıtımı, misyon-vizyon, kurallar</li>
  <li><strong>Departman Tanışması</strong> — Ekip üyeleri ile tanışma, departman süreçlerinin anlatılması</li>
  <li><strong>Ekipman Teslimi</strong> — Bilgisayar, telefon, kart, ofis malzemelerinin teslimi</li>
  <li><strong>Sistem Hesapları</strong> — E-posta, ERP, VPN ve diğer sistem hesaplarının açılması</li>
  <li><strong>Sözleşme İmzalanması</strong> — İş sözleşmesi, gizlilik sözleşmesi, etik kurallar</li>
  <li><strong>İlk Hafta Değerlendirmesi</strong> — Adaptasyon durumu, geri bildirim toplanması</li>
</ol>

<h4>Özellikler</h4>
<ul>
  <li>Otomatik onboarding süreci başlatma</li>
  <li>Adım bazında sorumlu atama</li>
  <li>İlerleme takibi ve yüzdesel gösterim</li>
  <li>Hatırlatma bildirimleri</li>
  <li>Onboarding raporu</li>
</ul>

