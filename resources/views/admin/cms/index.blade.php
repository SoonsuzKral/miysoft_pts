function submitBlogForm(url = CMS_URLS.blog.store, method = 'POST') {
    const form = document.getElementById('blogForm');
    const editor = document.getElementById('contentEditor');
    const hidden = document.getElementById('contentHidden');
    if (editor && hidden) hidden.value = editor.innerHTML;

    const fd = new FormData(form);
    fd.delete('is_published');
    fd.append('is_published', form.querySelector('[name="is_published"]')?.checked ? '1' : '0');

    const progressWrap = document.getElementById('uploadProgress');
    const progressBar = document.getElementById('progressBar');
    const cfg = {
        headers: { 'Content-Type': 'multipart/form-data', 'Accept': 'application/json', 'X-Requested-With': 'XMLHttpRequest' },
        onUploadProgress: (e) => {
            if (!e.total || !progressWrap || !progressBar) return;
            progressWrap.classList.remove('hidden');
            progressBar.style.width = Math.round((e.loaded * 100) / e.total) + '%';
        },
    };

    const run = method === 'PUT'
        ? (() => { fd.append('_method', 'PUT'); return axios.post(url, fd, cfg); })()
        : axios.post(url, fd, cfg);

    run.then(res => {
        if (progressWrap) { progressWrap.classList.add('hidden'); progressBar.style.width = '0%'; }
        document.getElementById('globalModal').classList.add('hidden');
        toast('success', res.data.message);
        loadBlogs();
    }).catch(err => {
        if (progressWrap) { progressWrap.classList.add('hidden'); if (progressBar) progressBar.style.width = '0%'; }
        const d = err.response?.data;
        const msg = d?.message || (d?.errors && Object.values(d.errors).flat().join(' ')) || 'Kayıt başarısız';
        toast('error', msg);
    });
}

