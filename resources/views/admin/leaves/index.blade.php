    loadLeaveRequests();
    loadKpiCounts();
});

function loadLeaveRequests(page = 1) {
    const params = {
        page,
        personel_id:    document.getElementById('filterPersonel').value,
        leave_type_id:  document.getElementById('filterLeaveType').value,
        status:         document.getElementById('filterStatus').value,
        date_from:      document.getElementById('filterDateFrom').value,
        per_page: 15,
    };
    axios.get(LEAVE_URLS.list, { params }).then(res => renderLeaveTable(res.data));
}

function loadKpiCounts() {
    ['pending','approved','rejected','cancelled'].forEach(s => {
        axios.get(LEAVE_URLS.list, { params: { status: s, per_page: 1 } }).then(res => {
            const el = document.getElementById(`kpi-${s}`);
            if (el) el.textContent = res.data.total;
        });
    });
}

const statusConfig = {
    pending:   { label: 'Bekliyor',     bg: 'bg-yellow-100', text: 'text-yellow-700' },
    approved:  { label: 'Onaylandı',    bg: 'bg-green-100',  text: 'text-green-700' },
    rejected:  { label: 'Reddedildi',   bg: 'bg-red-100',    text: 'text-red-700' },
    cancelled: { label: 'İptal',        bg: 'bg-gray-100',   text: 'text-gray-600' },
};

