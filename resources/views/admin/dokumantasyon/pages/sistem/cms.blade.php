<h4>Özellikler</h4>
<ul>
  <li>WYSIWYG zengin metin editörü</li>
  <li>SEO dostu URL (slug) yönetimi</li>
  <li>Taslak ve yayın durumu</li>
  <li>Zamanlanmış yayın</li>
  <li>Revizyon geçmişi ve geri alma</li>
  <li>SSS/FAQ kategorilendirme</li>
</ul>

<h4>İzinler</h4>
<p><code>cms.list</code>, <code>cms.create</code>, <code>cms.edit</code>, <code>cms.delete</code>, <code>cms.publish</code> (yayınlama)</p>
