<h3>🖼️ Medya Kütüphanesi</h3>

<p>Medya Kütüphanesi modülü, sistemde kullanılan tüm görsel ve doküman dosyalarının merkezi olarak yüklendiği, düzenlendiği ve yönetildiği bölümdür. Görseller (JPG, PNG, GIF, SVG, WebP), dokümanlar (PDF, DOC, XLS) ve diğer dosya türleri bu modüle yüklenebilir. Dosyalar klasör yapısı içinde organize edilebilir ve etiketlenebilir.</p>

<p>Yüklenen medya dosyaları diğer modüller (duyuru, CMS, personel kartı vb.) tarafından kullanılabilir. Dosya boyutu sınırlaması, izin verilen dosya türleri ve depolama alanı takibi yapılır. Her dosya için küçük boyutlu (thumbnail) görseller otomatik oluşturulur. Toplu dosya yükleme ve sürükle-bırak desteği sunar.</p>

