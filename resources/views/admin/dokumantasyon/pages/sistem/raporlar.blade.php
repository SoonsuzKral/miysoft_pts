<h3>📊 Raporlar</h3>

<p>Raporlar modülü, sistemdeki tüm modüllerden toplanan verilerin merkezi olarak raporlandığı ve analiz edildiği bölümdür. Personel raporları, puantaj raporları, izin raporları, finansal raporlar, lokasyon raporları gibi birçok farklı rapor türü bu modülden oluşturulabilir. Her rapor için tarih aralığı, departman, personel gibi filtreleme seçenekleri sunulur.</p>

<p>Raporlar grafiksel gösterimler (çubuk grafik, pasta grafik, çizgi grafik) ve tablo görünümü olarak iki farklı şekilde görüntülenebilir. Tüm raporlar PDF, Excel, CSV formatlarında dışa aktarılabilir. Sık kullanılan raporlar kaydedilebilir ve zamanlanmış rapor gönderme ayarlanabilir.</p>

