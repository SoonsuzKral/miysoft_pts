<h3>🚀 Süreç / Onboarding</h3>

<p>Onboarding (İşe Alıştırma) Süreci, yeni işe başlayan personellerin şirkete adaptasyonunu kolaylaştırmak için tasarlanmış adım adım bir süreç yönetim modülüdür. Personel işe giriş yaptığında sistem otomatik olarak bir onboarding süreci başlatır ve her adımın tamamlanması takip edilir.</p>

<p>Süreç adımları; oryantasyon eğitimi, departman tanışması, ekipman teslimi, sistem hesaplarının oluşturulması ve sözleşme imzalanması gibi görevlerden oluşur. Her adım için sorumlu kişi ve son tarih belirlenebilir. Adımlar tamamlandıkça ilerleme yüzdesi otomatik güncellenir.</p>

