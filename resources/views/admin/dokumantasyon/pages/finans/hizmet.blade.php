<h4>Özellikler</h4>
<ul>
  <li>Taşeron firma ve hizmet sağlayıcı kaydı</li>
  <li>Sözleşme yönetimi ve yenileme hatırlatıcıları</li>
  <li>Taşeron personel takibi</li>
  <li>Hizmet bedeli ve fatura takibi</li>
  <li>Performans değerlendirme</li>
  <li>Sözleşme sona erme bildirimleri</li>
</ul>

