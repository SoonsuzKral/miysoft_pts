function openApproveModal(id) {
    document.getElementById('approvalModalTitle').textContent = '✅ İzni Onayla';
    document.getElementById('approvalModalContent').innerHTML = `
        <p class="text-sm text-gray-600 mb-3">Bu izin talebini onaylamak istiyor musunuz?</p>
        <label class="block text-sm font-medium text-gray-700 mb-1">Not (isteğe bağlı)</label>
        <textarea id="approvalNote" rows="2" class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-[#02E0FB]" placeholder="Onay notu..."></textarea>
    `;
    const btn = document.getElementById('approvalModalBtn');
    btn.textContent = 'Onayla';
    btn.className = 'px-4 py-2 text-sm text-white bg-green-600 hover:bg-green-700 rounded-lg font-medium';
    btn.onclick = () => submitApproval(id, 'approve');
    document.getElementById('approvalModal').classList.remove('hidden');
}

function openRejectModal(id) {
    document.getElementById('approvalModalTitle').textContent = '❌ İzni Reddet';
    document.getElementById('approvalModalContent').innerHTML = `
        <p class="text-sm text-gray-600 mb-3">Red gerekçesini belirtiniz.</p>
        <label class="block text-sm font-medium text-gray-700 mb-1">Ret Gerekçesi <span class="text-red-500">*</span></label>
        <textarea id="approvalNote" rows="3" required class="w-full px-3 py-2 text-sm border border-gray-200 rounded-lg focus:outline-none focus:border-red-400" placeholder="Reddetme gerekçesini yazınız..."></textarea>
    `;
    const btn = document.getElementById('approvalModalBtn');
    btn.textContent = 'Reddet';
    btn.className = 'px-4 py-2 text-sm text-white bg-red-600 hover:bg-red-700 rounded-lg font-medium';
    btn.onclick = () => submitApproval(id, 'reject');
    document.getElementById('approvalModal').classList.remove('hidden');
}

function submitApproval(id, action) {
    const note = document.getElementById('approvalNote')?.value;
    if (action === 'reject' && !note?.trim()) {
        toast('warning', 'Ret gerekçesi zorunludur.');
        return;
    }

    const url     = action === 'approve' ? LEAVE_URLS.approve(id) : LEAVE_URLS.reject(id);
    const payload = action === 'approve' ? { note } : { reason: note };

    axios.post(url, payload).then(res => {
        closeApprovalModal();
        toast(res.data.success ? 'success' : 'error', res.data.message);
        loadLeaveRequests();
        loadKpiCounts();
    });
}

function cancelLeave(id) {
    Swal.fire({
        title: 'İzni İptal Et',
        input: 'textarea',
        inputPlaceholder: 'İptal gerekçesi (isteğe bağlı)...',
        showCancelButton: true,
        confirmButtonColor: '#FA6001',
        cancelButtonColor: '#6B7280',
        confirmButtonText: 'İptal Et',
        cancelButtonText: 'Vazgeç',
    }).then(result => {
        if (result.isConfirmed) {
            axios.post(LEAVE_URLS.cancel(id), { reason: result.value }).then(res => {
                toast(res.data.success ? 'success' : 'error', res.data.message);
                loadLeaveRequests();
                loadKpiCounts();
            });
        }
    });
}

function closeApprovalModal() {
    document.getElementById('approvalModal').classList.add('hidden');
}

// ─── Create / Edit ────────────────────────────────────────────────────────────

function openCreateLeaveModal() {
    axios.get(LEAVE_URLS.create).then(res => {
        document.getElementById('modalTitle').textContent = 'Yeni İzin Talebi';
        document.getElementById('modalBody').innerHTML = res.data.html;
        document.getElementById('modalFooter').innerHTML = `
            <button onclick="document.getElementById('globalModal').classList.add('hidden')" class="px-4 py-2 text-sm text-gray-600 hover:bg-gray-100 rounded-lg">İptal</button>
            <button onclick="submitLeaveForm()" class="px-4 py-2 text-sm text-white bg-[#02E0FB] hover:bg-cyan-400 rounded-lg font-medium">Talep Oluştur</button>`;
        document.getElementById('globalModal').classList.remove('hidden');
    });
}

function submitLeaveForm(url = LEAVE_URLS.store, method = 'POST') {
    const form = document.getElementById('leaveRequestForm');
    const data = Object.fromEntries(new FormData(form).entries());
    axios({ method, url, data }).then(res => {
        document.getElementById('globalModal').classList.add('hidden');
        toast(res.data.auto_approved ? 'success' : 'info', res.data.message);
        loadLeaveRequests();
        loadKpiCounts();
    });
}
