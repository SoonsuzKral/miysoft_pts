<h4>Rapor Türleri</h4>
<ul>
  <li><strong>Personel Raporları</strong> — Personel listesi, dağılım, yaş/cinsiyet istatistikleri</li>
  <li><strong>Puantaj Raporları</strong> — Aylık devam, geç kalma, fazla mesai</li>
  <li><strong>İzin Raporları</strong> — İzin kullanımı, bakiye raporları</li>
  <li><strong>Finans Raporları</strong> — Avans, masraf, seyahat maliyetleri</li>
  <li><strong>Lokasyon Raporları</strong> — Lokasyon bazlı personel dağılımı</li>
</ul>

<h4>Özellikler</h4>
<ul>
  <li>Merkezi raporlama arayüzü</li>
  <li>Grafiksel ve tablo görünümü</li>
  <li>Gelişmiş filtreleme</li>
  <li>PDF/Excel/CSV dışa aktarma</li>
  <li>Kaydedilmiş raporlar ve zamanlanmış gönderme</li>
</ul>

<h4>İzinler</h4>
<p><code>rapor.list</code>, <code>rapor.view</code>, <code>rapor.export</code>, <code>rapor.schedule</code> (zamanlanmış rapor)</p>
