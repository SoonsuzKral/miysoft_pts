<h3>✈️ Seyahat Yönetimi</h3>

<p>Seyahat Yönetimi modülü, personellerin iş seyahatlerinin planlanması, onaylanması ve takip edilmesini sağlar. Seyahat talebi oluşturulurken gidiş/dönüş tarihleri, varış noktası, seyahat amacı, ulaşım aracı (uçak, tren, araç) ve konaklama bilgileri girilir. Her seyahat için bir güzergah (itinerary) oluşturulur.</p>

<p>Onay sürecinden geçen seyahatler için bilet rezervasyonu, otel konaklaması ve araç kiralama gibi işlemler takip edilebilir. Seyahat sonrası masraf bildirimi doğrudan seyahat kaydına bağlanabilir. Seyahat raporları sayesinde toplam seyahat maliyeti, sıklığı ve dağılımı analiz edilebilir.</p>

