<h3>📦 Envanter & Zimmet</h3>

<p>Envanter & Zimmet modülü, şirketin sahip olduğu ürün, ekipman ve demirbaşların kayıt altına alındığı ve personellere zimmetlenmesinin takip edildiği modüldür. Dizüstü bilgisayar, telefon, araç, mobilya gibi tüm fiziksel varlıklar bu modüle kaydedilir. Her envanter kalemi için marka, model, seri numarası, satın alma tarihi ve garanti bilgileri tutulur.</p>

<p>Zimmet işlemi ile bir envanter kalemi belirli bir personele atanır. Zimmet başlangıç ve bitiş tarihleri kaydedilir, envanter iade alındığında zimmet kapatılır. Stok raporları sayesinde hangi envanterlerin kimde olduğu, hangilerinin boşta olduğu görüntülenebilir. Zimmet geçmişi tüm hareketlerle birlikte saklanır.</p>

