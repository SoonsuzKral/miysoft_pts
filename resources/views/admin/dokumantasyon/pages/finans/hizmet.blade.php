<h3>🔧 Hizmetler (Taşeron Yönetimi)</h3>

<p>Hizmetler modülü, şirkete dış kaynak olarak hizmet veren taşeron firmaların ve bağımsız hizmet sağlayıcıların yönetilmesini sağlar. Her hizmet sağlayıcı için firma adı, yetkili kişi, iletişim bilgileri, sözleşme başlangıç/bitiş tarihleri ve hizmet kapsamı kaydedilir.</p>

<p>Taşeron personellerinin şirket içindeki hareketleri, görev alanları ve çalışma saatleri takip edilebilir. Hizmet sözleşmeleri yenileme hatırlatıcıları, hizmet bedeli takibi ve performans değerlendirmesi gibi özellikler sayesinde taşeron yönetimi merkezi bir noktadan gerçekleştirilir.</p>

