    const tbody = document.getElementById('leaveTableBody');
    if (!data.data.length) {
        tbody.innerHTML = `<tr><td colspan="7" class="px-4 py-8 text-center text-gray-400 text-sm">Kayıt bulunamadı</td></tr>`;
        return;
    }
    tbody.innerHTML = data.data.map(r => {
        const sc = statusConfig[r.status] || { label: r.status, bg: 'bg-gray-100', text: 'text-gray-600' };
        const isPending = r.status === 'pending';
        const startDate = new Date(r.start_date).toLocaleDateString('tr-TR');
        const endDate   = new Date(r.end_date).toLocaleDateString('tr-TR');

        return `
        <tr class="hover:bg-gray-50 transition-colors ${isPending ? 'bg-yellow-50/30' : ''}">
            <td class="px-4 py-3">
