function openBlogEdit(id) {
    axios.get(CMS_URLS.blog.edit(id)).then(res => {
        document.getElementById('modalTitle').textContent = 'Blog Yazısını Düzenle';
        document.getElementById('modalBody').innerHTML = res.data.html;
