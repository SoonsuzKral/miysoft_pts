<h4>Özellikler</h4>
<ul>
  <li>Seyahat talep formu (tarih, yer, amaç)</li>
  <li>Ulaşım ve konaklama bilgisi</li>
  <li>Güzergah (itinerary) oluşturma</li>
  <li>Çok aşamalı onay süreci</li>
  <li>Masraf entegrasyonu</li>
  <li>Seyahat maliyet raporları</li>
</ul>

