<h3>📅 Tatil Takvimi</h3>

<p>Tatil Takvimi modülü, resmî tatil günleri ve şirkete özel tatil günlerinin tanımlandığı ve yönetildiği bölümdür. Ulusal bayramlar, dini bayramlar ve resmî tatil günleri sistem tarafından otomatik olarak önerilir. Şirket bazında özel tatil günleri (kuruluş yıldönümü, bakım günü gibi) manuel olarak eklenebilir.</p>

<p>Tatil günleri puantaj modülü ile entegre çalışır. Tatil günlerinde puantaj otomatik olarak tatil olarak işaretlenir, izin hesaplamalarında tatil günleri izin gününden sayılmaz. Takvim üzerinde renk kodlu gösterim sayesinde tüm tatil günleri görsel olarak takip edilebilir.</p>

