<h4>Özellikler</h4>
<ul>
  <li>Avans talep oluşturma</li>
  <li>Çok aşamalı onay süreci</li>
  <li>Personel bazında avans limiti ve bakiye takibi</li>
  <li>Avans ödeme kaydı</li>
  <li>Maaş bordrosu entegrasyonu</li>
  <li>Detaylı avans raporları</li>
</ul>

