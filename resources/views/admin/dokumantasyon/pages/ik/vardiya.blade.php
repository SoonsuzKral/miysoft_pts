<h3>🔄 Vardiya Yönetimi</h3>

<p>Vardiya Yönetimi modülü, farklı vardiya düzenlerini tanımlamanıza ve personellerinize atamanıza olanak tanır. Sabit vardiya, dönüşümlü vardiya, esnek vardiya ve gece vardiyası gibi farklı vardiya türleri oluşturabilir, her vardiya için başlangıç ve bitiş saatlerini belirleyebilirsiniz. Vardiyalar takvim üzerinde renk kodlu olarak görüntülenir.</p>

<p>Personel-vardiya ataması toplu veya bireysel olarak yapılabilir. Vardiya bazlı puantaj takibi sayesinde personelin hangi vardiyada çalıştığı otomatik olarak puantaj kayıtlarına yansır. Vardiya değişiklik talepleri ve onay süreci de bu modül üzerinden yönetilir.</p>

