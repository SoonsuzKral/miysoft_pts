<h3>🚪 Ziyaretçi Yönetimi</h3>

<p>Ziyaretçi Yönetimi modülü, şirkete gelen misafirlerin giriş-çıkış işlemlerinin kayıt altına alınmasını sağlar. Ziyaretçi kartı oluşturulurken ziyaretçinin adı, soyadı, TC/pasaport numarası, şirketi, görüşeceği kişi, ziyaret amacı ve giriş saati kaydedilir. Ziyaretçi çıkış yaptığında çıkış saati otomatik işlenir.</p>

<p>Ziyaretçi kartı yazdırılabilir ve ziyaretçinin fotoğrafı çekilerek karta eklenebilir. Ziyaretçi geçmişi sorgulanabilir, belirli bir tarih aralığında gelen tüm ziyaretçiler raporlanabilir. Görüşülen personel (host) bilgisi sayesinde kimin hangi ziyaretçiyi ağırladığı takip edilebilir.</p>

