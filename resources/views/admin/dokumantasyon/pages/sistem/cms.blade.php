<h3>📄 İçerik Yönetimi (CMS)</h3>

<p>İçerik Yönetimi modülü, sistemde görüntülenen statik sayfaların, şirket politikalarının ve sıkça sorulan soruların (SSS/FAQ) yönetildiği bölümdür. Şirket tanıtım sayfası, gizlilik politikası, etik kurallar, işe alım politikası gibi sayfalar bu modül üzerinden oluşturulur ve düzenlenir. Zengin metin editörü (WYSIWYG) ile içerikler kolayca biçimlendirilebilir.</p>

<p>Her sayfa için SEO dostu URL (slug), meta açıklama ve yayın durumu belirlenebilir. Sayfalar taslak olarak kaydedilebilir, belirli bir tarihte yayına alınabilir. Sayfa revizyon geçmişi tutulur ve eski sürümlere dönülebilir. Personeller anasayfada veya mobil uygulamada bu sayfaları görüntüleyebilir.</p>

