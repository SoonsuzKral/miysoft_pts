<h3>🧾 Masraf Yönetimi</h3>

<p>Masraf Yönetimi modülü, personellerin iş amaçlı yaptığı harcamaların (yakıt, yemek, kırtasiye, seyahat masrafları vb.) kaydedildiği ve onaylandığı modüldür. Personel masraf bildirimi oluşturur, fiş/fatura görsellerini yükler, masraf kategorisini seçer ve onaya gönderir. Masraf talepleri yönetici ve finans departmanı tarafından incelenir ve onaylanır/reddedilir.</p>

<p>Her masraf kalemi için kategori, tarih, miktar, döviz türü ve açıklama bilgileri tutulur. Fatura/fiş görselleri sisteme yüklenir ve arşivlenir. Masraf raporları departman bazında, tarih aralığına göre filtrelenebilir ve Excel/PDF olarak dışa aktarılabilir.</p>

