<h4>Özellikler</h4>
<ul>
  <li>Ziyaretçi kartı oluşturma ve yazdırma</li>
  <li>Fotoğraflı ziyaretçi kaydı</li>
  <li>Host (görüşülen kişi) atama ve bilgilendirme</li>
  <li>Giriş/çıkış saat takibi</li>
  <li>Ziyaretçi geçmişi ve raporlama</li>
  <li>Toplu ziyaretçi listesi</li>
</ul>

