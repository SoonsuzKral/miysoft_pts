<h3>📢 Duyurular & Anketler</h3>

<p>Duyuru ve Anket modülü, şirket içi iletişimi güçlendirmek amacıyla duyuruların yayınlanması ve anketlerin oluşturulmasını sağlar. Duyurular tüm personele veya belirli departmanlara hedeflenebilir, önem seviyesi belirlenebilir ve yayın süresi ayarlanabilir. Duyuruya görsel ve dosya eklenebilir.</p>

<p>Anketler ile personel görüşleri toplanabilir. Çoktan seçmeli, açık uçlu, likert ölçeği gibi farklı soru tipleri desteklenir. Anket katılım oranı ve sonuçları anlık olarak görüntülenebilir, Excel/PDF olarak dışa aktarılabilir. Anonym (anonim) anket seçeneği ile hassas konularda geri bildirim alınabilir.</p>

