<h3>💰 Avans Yönetimi</h3>

<p>Avans Yönetimi modülü, personellerin maaşlarına karşılık talep ettiği avans ödemelerinin yönetildiği modüldür. Personel, belirli bir miktar için avans talebi oluşturur, talep yönetici ve finans departmanı onayından geçtikten sonra ödeme gerçekleştirilir. Her avans talebi için talep tarihi, miktar, açıklama ve ödeme durumu kaydedilir.</p>

<p>Modül, personel bazında avans limiti kontrolü yapar. Personelin daha önce aldığı avanslar, geri ödeme durumu ve kalan limit bilgisi otomatik hesaplanır. Avans ödemeleri maaş bordrosuna yansıtılmak üzere işaretlenebilir. Tüm avans hareketleri detaylı raporlanabilir.</p>

